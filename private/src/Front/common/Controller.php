abstract class Controller extends CoreController {

	/**
	 * Constructor
	 *
	 * @access public
	 * @return object
	 */

	public function __construct() {

		parent::__construct();
	}
	
	/**
	 * Constructor
	 *
	 * @access public
	 * @return object
	 */
	protected function _checkSession()
	{
		if (!$this->session->get('id_user')) {
		    
		    $this->redirect($this->url->getUrl('home'));
		}
	}
}
